package com.cockpitconfig.objects;

public class Communication {

	private int id;
	private String medium;

	public Communication() {}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMedium() {
		return medium;
	}

	public void setMedium(String medium) {
		this.medium = medium;
	}
}
